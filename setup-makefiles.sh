#!/usr/bin/env bash
set -euo pipefail

DEVICE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
VENDOR_DIR="${1:-${ANDROID_BUILD_TOP:-$(cd "$DEVICE_DIR/../../.." && pwd)}/vendor/onyx/leaf3}"
BLOBS="$VENDOR_DIR/proprietary/vendor"
OUT="$VENDOR_DIR/leaf3-vendor.mk"

[[ -d "$BLOBS" ]] || { echo "Missing extracted blobs: $BLOBS" >&2; exit 1; }
mkdir -p "$VENDOR_DIR"

{
  echo 'LOCAL_PATH := vendor/onyx/leaf3'
  echo
  printf '%s\n' 'PRODUCT_COPY_FILES += \'
  find "$BLOBS" \( -type f -o -type l \) -print | LC_ALL=C sort | while IFS= read -r source; do
    relative="${source#"$VENDOR_DIR/proprietary/"}"
    # These outputs are generated by the platform build and cannot be copied.
    # Vendor Java apps need dedicated prebuilt modules; they are intentionally
    # excluded during native-hardware first boot bring-up.
    case "$relative" in
      vendor/build.prop|vendor/default.prop|vendor/etc/fstab.qcom|vendor/etc/vintf/*|*.apk|*.jar|*.odex|*.vdex) continue;;
      # AOSP/Lineage installs these utilities itself. Stock copies would
      # overwrite build targets and are not device-specific blobs.
      vendor/bin/acpi|vendor/bin/applypatch|vendor/bin/basename|vendor/bin/blkid|vendor/bin/chcon|vendor/bin/chmod|vendor/bin/chown|vendor/bin/date|vendor/bin/dmesg|vendor/bin/grep|vendor/bin/gzip|vendor/bin/insmod|vendor/bin/kill|vendor/bin/linker|vendor/bin/logd|vendor/bin/mkdir|vendor/bin/mount|vendor/bin/ping|vendor/bin/reboot|vendor/bin/rmdir|vendor/bin/setenforce|vendor/bin/sleep|vendor/bin/sync|vendor/bin/toybox|vendor/bin/umount|vendor/bin/vendor_init) continue;;
    esac
    printf '    $(LOCAL_PATH)/proprietary/%s:$(TARGET_COPY_OUT_VENDOR)/%s \\\n' "${relative#vendor/}" "${relative#vendor/}"
  done
  echo
  # VINTF XML is assembled by the build system, rather than copied as a raw
  # vendor file.  The stock Android 11 declarations match the stock HALs.
  if [[ -f "$BLOBS/etc/vintf/manifest.xml" ]]; then
    echo 'DEVICE_MANIFEST_FILE += $(LOCAL_PATH)/proprietary/vendor/etc/vintf/manifest.xml'
  fi
  if [[ -f "$BLOBS/etc/vintf/compatibility_matrix.xml" ]]; then
    echo 'DEVICE_MATRIX_FILE += $(LOCAL_PATH)/proprietary/vendor/etc/vintf/compatibility_matrix.xml'
  fi
} > "$OUT"
